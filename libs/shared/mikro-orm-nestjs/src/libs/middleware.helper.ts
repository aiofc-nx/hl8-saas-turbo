import { type MiddlewareConsumer, HttpStatus } from '@nestjs/common';
import type {
  MikroOrmMiddlewareModuleOptions,
  NestMiddlewareConsumer,
} from './typings.js';

export function forRoutesPath(
  options: MikroOrmMiddlewareModuleOptions,
  consumer: MiddlewareConsumer,
) {
  if (options.forRoutesPath) {
    return options.forRoutesPath;
  }

  // detect nest v11 based on a newly added enum value
  if (HttpStatus.MULTI_STATUS) {
    return '{*all}';
  }

  const isFastify = (consumer: MiddlewareConsumer) => {
    const nestConsumer = consumer as NestMiddlewareConsumer;
    if (typeof nestConsumer.httpAdapter !== 'object') {
      return false;
    }

    return nestConsumer.httpAdapter.constructor.name
      .toLowerCase()
      .startsWith('fastify');
  };

  return isFastify(consumer) ? '(.*)' : '*';
}
